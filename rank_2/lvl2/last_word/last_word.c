#include <unistd.h>

int main(int argc, char **argv)
{
	int	i = 0;

	if (argc == 2)
	{
		if (argv[1][i] == ' ' || (argv[1][i] >= 9 && argv[1][i] <= 13))
			i++;
	}
	return (0);
}
